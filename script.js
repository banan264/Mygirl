const heartBtn = document.getElementById('heartBtn');
const heartsContainer = document.getElementById('hearts');
const sparklesContainer = document.getElementById('sparkles');
const title = document.getElementById('title');
const message = document.getElementById('message');

// –ê–Ω–∏–º–∏—Ä–æ–≤–∞–Ω–Ω–∞—è –ø–µ—á–∞—Ç—å —Ç–µ–∫—Å—Ç–∞
const textTitle = "–¢—ã ‚Äî –º–æ–µ —Å—á–∞—Å—Ç—å–µ üíñ";
const textMessage = "–ö–∞–∂–¥—ã–π –¥–µ–Ω—å —Å —Ç–æ–±–æ–π ‚Äî –∫–∞–∫ –ø—Ä–∞–∑–¥–Ω–∏–∫ ‚ú®";

let i = 0;
function typeWriter() {
    if (i < textTitle.length) {
        title.innerHTML += textTitle.charAt(i);
        i++;
        setTimeout(typeWriter, 100);
    } else {
        fadeInMessage();
    }
}

function fadeInMessage() {
    let j = 0;
    function typeMsg() {
        if (j < textMessage.length) {
            message.innerHTML += textMessage.charAt(j);
            j++;
            setTimeout(typeMsg, 50);
        }
    }
    typeMsg();
}

window.onload = typeWriter;

// –°–µ—Ä–¥–µ—á–∫–∏
heartBtn.addEventListener('click', () => {
    for (let i = 0; i < 50; i++) {
        createHeart();
    }
});

function createHeart() {
    const heart = document.createElement('div');
    heart.classList.add('heart');
    heart.innerText = '‚ù§Ô∏è';
    heart.style.left = Math.random() * window.innerWidth + 'px';
    heart.style.fontSize = 20 + Math.random() * 40 + 'px';
    heart.style.animationDuration = 3 + Math.random() * 4 + 's';
    heartsContainer.appendChild(heart);
    setTimeout(() => { heart.remove(); }, 5000);
}

